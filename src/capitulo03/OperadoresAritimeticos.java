package capitulo03;

public class OperadoresAritimeticos {
	public static void main(String[] args) {
		int number01 = 10;//NUMBER RECEBE 125
		int number02 = 3;
		
		//OPRADOR DE MULTIPLICAÇÃO
		System.out.println(number01 * number02);
		
		//OPERADOR DE DIVISÃO
		System.out.println(number01 / number02);
		
		//OPERADOR DE MÓDULO DA DIVIÃO
		System.out.println(number01 % number02);
		
		//OPERADOR DE SOMA
		System.out.println(number01 + number02);
		
		//OPERADOR DE SUBTRAÇÃO
		System.out.println(number01 - number02);
	}
}
